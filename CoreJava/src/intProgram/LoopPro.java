package intProgram;

public class LoopPro {

	public static void main(String[] args) {
		int i;
		int j;
		for (i=1;i<=5;i++) {
			
			for (j=1;j<+i;j++) {
				System.out.print(j);
			}
			System.out.println();
			}
		String s= "";
		System.out.println(s.getClass().getName());
	}
}
